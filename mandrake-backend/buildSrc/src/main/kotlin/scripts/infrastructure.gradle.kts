package scripts

plugins {
    id("distribution")
}

tasks.named<Wrapper>("wrapper") {
    gradleVersion = BuildPlugins.Versions.gradleVersion
    distributionType = Wrapper.DistributionType.ALL
}

tasks.register("runApp") {
    description = "Runs App in Development Mode."
    dependsOn("run")
}

tasks.register("runUnitTests") {
    description = "Runs all Unit Tests."
    dependsOn("test")
}

// -------------------------------------------------------------------------------------
// Heroku Deployment
// Info from https://dev.to/kotlin/publishing-server-side-kotlin-applications-ktor-on-heroku-2ce4
// -------------------------------------------------------------------------------------
distributions {
    main {
        contents {
            from("$buildDir/libs") {
                rename("${rootProject.name}-jvm", rootProject.name)
                into("lib")
            }
        }
    }
}

tasks.create("stage") {
    dependsOn("installDist")
}